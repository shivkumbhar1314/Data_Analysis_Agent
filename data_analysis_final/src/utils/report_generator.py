    def _generate_sections_html(self, agent_results: Dict[str, Any]) -> str:
        html = ""
        
        for agent_name, result in agent_results.items():
            if isinstance(result, dict):
                html += f"""
        <div class="agent-section">
            <h2>{agent_name}</h2>
            {self._dict_to_html(result)}
        </div>
                """
        
        return html
    
    def _dict_to_html(self, data: Any, level: int = 0) -> str:
        if isinstance(data, dict):
            html = ""
            for key, value in data.items():
                if isinstance(value, (list, dict)):
                    html += f"<h3>{key}</h3>"
                    html += self._dict_to_html(value, level + 1)
                else:
                    html += f"<div class='metric'><strong>{key}:</strong> {value}</div>"
            return html
        elif isinstance(data, list):
            if len(data) == 0:
                return ""
            
            if isinstance(data[0], dict):
                html = "<table>"
                # Header
                headers = list(data[0].keys())
                html += "<tr>" + "".join(f"<th>{h}</th>" for h in headers) + "</tr>"
                # Rows
                for item in data:
                    html += "<tr>" + "".join(f"<td>{item.get(h, '')}</td>" for h in headers) + "</tr>"
                html += "</table>"
                return html
            else:
                return "<ul>" + "".join(f"<li>{item}</li>" for item in data) + "</ul>"
        else:
            return str(data)
    
    def generate_json_report(self, agent_results: Dict[str, Any], 
                            dataset_name: str = "Analysis") -> str:
        report_data = {
            'timestamp': datetime.now().isoformat(),
            'dataset_name': dataset_name,
            'agent_results': agent_results,
        }
        
        report_filename = f"report_{dataset_name}_{datetime.now().strftime('%Y%m%d_%H%M%S')}.json"
        report_path = self.output_dir / report_filename
        
        with open(report_path, 'w', encoding='utf-8') as f:
            json.dump(report_data, f, indent=2, default=str)
        
        return str(report_path)
    
    def generate_summary(self, agent_results: Dict[str, Any]) -> str:
        summary = "=== Data Analysis Summary ===\n\n"
        
        for agent_name, result in agent_results.items():
            summary += f"\n{agent_name}:\n"
            summary += "-" * len(agent_name) + "\n"
            
            if isinstance(result, dict):
                summary += self._dict_to_text(result, indent=2)
        
        return summary
    
    def _dict_to_text(self, data: Any, indent: int = 0) -> str:
        text = ""
        prefix = " " * indent
        
        if isinstance(data, dict):
            for key, value in data.items():
                if isinstance(value, (list, dict)):
                    text += f"{prefix}{key}:\n"
                    text += self._dict_to_text(value, indent + 2)
                else:
                    text += f"{prefix}{key}: {value}\n"
        elif isinstance(data, list):
            for item in data:
                if isinstance(item, dict):
                    text += self._dict_to_text(item, indent)
                else:
                    text += f"{prefix}â€¢ {item}\n"
        
        return text
